function navigateTo(section) {
  if (section === 'home') {
    window.location.href = 'https://mykhaylofilip.com';
  } else if (section === 'posts') {
    window.location.href = 'https://mykhaylofilip.com/posts';
  } else if (section === 'other') {
    window.location.href = 'https://mykhaylofilip.com/etc';
  }
}
